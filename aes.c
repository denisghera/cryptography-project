static const uint8_t rcon[11] = {0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36};

static void sub_word(uint8_t *word) {
    for (int i = 0; i < 4; i++) word[i] = s_box[word[i]];
}

static void rot_word(uint8_t *word) {
    uint8_t tmp = word[0];
    word[0] = word[1];
    word[1] = word[2];
    word[2] = word[3];
    word[3] = tmp;
}

void key_expansion(const uint8_t *key, uint8_t *expanded_key) {
    memcpy(expanded_key, key, 16);
    for (int i = 1; i < 11; i++) {
        uint8_t *prev = expanded_key + (i-1)*16;
        uint8_t *curr = expanded_key + i*16;
        uint8_t temp[4] = {prev[12], prev[13], prev[14], prev[15]};
        rot_word(temp);
        sub_word(temp);
        temp[0] ^= rcon[i];
        for (int j = 0; j < 4; j++) curr[j] = prev[j] ^ temp[j];
        for (int j = 4; j < 16; j++) curr[j] = curr[j-4] ^ prev[j];
    }
}

static void sub_bytes(uint8_t *state) {
    for (int i = 0; i < 16; i++) state[i] = s_box[state[i]];
}

static void inv_sub_bytes(uint8_t *state) {
    for (int i = 0; i < 16; i++) state[i] = inv_s_box[state[i]];
}

static void shift_rows(uint8_t *state) {
    uint8_t tmp[16] = {
        state[0], state[5], state[10], state[15],
        state[4], state[9], state[14], state[3],
        state[8], state[13], state[2], state[7],
        state[12], state[1], state[6], state[11]
    };
    memcpy(state, tmp, 16);
}

static void inv_shift_rows(uint8_t *state) {
    uint8_t tmp[16] = {
        state[0], state[13], state[10], state[7],
        state[4], state[1], state[14], state[11],
        state[8], state[5], state[2], state[15],
        state[12], state[9], state[6], state[3]
    };
    memcpy(state, tmp, 16);
}

static uint8_t gmul(uint8_t a, uint8_t b) {
    uint8_t p = 0;
    for (int i = 0; i < 8; i++) {
        if (b & 1) p ^= a;
        uint8_t carry = a & 0x80;
        a <<= 1;
        if (carry) a ^= 0x1b;
        b >>= 1;
    }
    return p;
}

static void mix_columns(uint8_t *state) {
    for (int i = 0; i < 4; i++) {
        uint8_t s0 = state[i*4], s1 = state[i*4+1], s2 = state[i*4+2], s3 = state[i*4+3];
        state[i*4]   = gmul(0x02, s0) ^ gmul(0x03, s1) ^ s2 ^ s3;
        state[i*4+1] = s0 ^ gmul(0x02, s1) ^ gmul(0x03, s2) ^ s3;
        state[i*4+2] = s0 ^ s1 ^ gmul(0x02, s2) ^ gmul(0x03, s3);
        state[i*4+3] = gmul(0x03, s0) ^ s1 ^ s2 ^ gmul(0x02, s3);
    }
}

static void inv_mix_columns(uint8_t *state) {
    for (int i = 0; i < 4; i++) {
        uint8_t s0 = state[i*4], s1 = state[i*4+1], s2 = state[i*4+2], s3 = state[i*4+3];
        state[i*4]   = gmul(0x0e, s0) ^ gmul(0x0b, s1) ^ gmul(0x0d, s2) ^ gmul(0x09, s3);
        state[i*4+1] = gmul(0x09, s0) ^ gmul(0x0e, s1) ^ gmul(0x0b, s2) ^ gmul(0x0d, s3);
        state[i*4+2] = gmul(0x0d, s0) ^ gmul(0x09, s1) ^ gmul(0x0e, s2) ^ gmul(0x0b, s3);
        state[i*4+3] = gmul(0x0b, s0) ^ gmul(0x0d, s1) ^ gmul(0x09, s2) ^ gmul(0x0e, s3);
    }
}

static void add_round_key(uint8_t *state, const uint8_t *round_key) {
    for (int i = 0; i < 16; i++) state[i] ^= round_key[i];
}

void aes_encrypt_block(uint8_t *block, const uint8_t *expanded_key) {
    add_round_key(block, expanded_key);
    for (int i = 1; i < 10; i++) {
        sub_bytes(block);
        shift_rows(block);
        mix_columns(block);
        add_round_key(block, expanded_key + i*16);
    }
    sub_bytes(block);
    shift_rows(block);
    add_round_key(block, expanded_key + 160);
}

void aes_decrypt_block(uint8_t *block, const uint8_t *expanded_key) {
    add_round_key(block, expanded_key + 160);
    for (int i = 9; i >= 1; i--) {
        inv_shift_rows(block);
        inv_sub_bytes(block);
        add_round_key(block, expanded_key + i*16);
        inv_mix_columns(block);
    }
    inv_shift_rows(block);
    inv_sub_bytes(block);
    add_round_key(block, expanded_key);
}

void aes_cbc_encrypt(uint8_t *plaintext, size_t len, const uint8_t *key, const uint8_t *iv, uint8_t *ciphertext) {
    uint8_t expanded_key[176];
    key_expansion(key, expanded_key);
    uint8_t prev[16];
    memcpy(prev, iv, 16);
    for (size_t i = 0; i < len; i += 16) {
        for (int j = 0; j < 16; j++) plaintext[i+j] ^= prev[j];
        aes_encrypt_block(plaintext + i, expanded_key);
        memcpy(prev, plaintext + i, 16);
        memcpy(ciphertext + i, plaintext + i, 16);
    }
}

void aes_cbc_decrypt(uint8_t *ciphertext, size_t len, const uint8_t *key, const uint8_t *iv, uint8_t *plaintext) {
    uint8_t expanded_key[176];
    key_expansion(key, expanded_key);
    uint8_t prev[16];
    memcpy(prev, iv, 16);
    for (size_t i = 0; i < len; i += 16) {
        uint8_t block[16];
        memcpy(block, ciphertext + i, 16);
        aes_decrypt_block(block, expanded_key);
        for (int j = 0; j < 16; j++) block[j] ^= prev[j];
        memcpy(prev, ciphertext + i, 16);
        memcpy(plaintext + i, block, 16);
    }
}